    public boolean duplicatecheck(){
        
        dbconnect dbc = new dbconnect();
   try {
            String query = "SELECT * FROM positions WHERE position = '"+ position.getText()+"'";
            ResultSet resultSet = dbc.getData(query);
            if(resultSet.next()){
                pname= resultSet.getString("position");
                if(pname.equals(position.getText())){
                JOptionPane.showMessageDialog(null,"Position already existed");
                position.setText("");
                }

                return true;
            }
            else{
                return false;
            }
            
        } catch (SQLException ex) {
            System.out.println(""+ex);
            return false;
        }
    }
    
