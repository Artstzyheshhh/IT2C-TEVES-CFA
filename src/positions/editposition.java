    private void selectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_selectMouseClicked
        int rowIndex = postable.getSelectedRow();

        if (rowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select an item!");
        } else {
            try {
                dbconnect dbc = new dbconnect();
                TableModel tbl = postable.getModel();
                ResultSet rs = dbc.getData("SELECT * FROM positions WHERE pid = '" + tbl.getValueAt(rowIndex, 0) + "'");
                if (rs.next()) { 
                    id.setText(""+rs.getInt("uid"));
                    position.setText(""+rs.getString("position"));
                    term.setSelectedItem(""+rs.getString("term")); 
                }

            } catch (SQLException ex) {
                System.out.println("" + ex);
            }
        }
    }//GEN-LAST:event_selectMouseClicked

